# Segundo programa - Imprime Hello com o seu nome
#Entrada de dados - input
name = input("What's your name? ")
#Impressão das variáveis
#Primeira versão
print("Hello, " + name)
#Segunda maneira de imprimir
print("Hello,", name)
#Terceira maneira de imprimir
print('Hello, ', end="")
print(name)
#Quarta maneira de imprimir
print(f"Hello, {name}")

